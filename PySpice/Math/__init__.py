import math

####################################################################################################

def odd(x):
    return x & 1

def even(x):
    return not(odd(x))

####################################################################################################

def rms_to_amplitude(x):
    return x * math.sqrt(2) 

def amplitude_to_rms(x):
    return x / math.sqrt(2) 

